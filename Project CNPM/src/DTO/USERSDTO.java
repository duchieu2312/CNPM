package DTO;

public class USERSDTO {
	private int USER_ID;
	private String USER_DisplayName;
	private String USER_UserName;
	private String USER_Password;
	private int USER_idRole;
	public int getUSER_ID() {
		return USER_ID;
	}
	public void setUSER_ID(int uSER_ID) {
		USER_ID = uSER_ID;
	}
	public String getUSER_DisplayName() {
		return USER_DisplayName;
	}
	public void setUSER_DisplayName(String uSER_DisplayName) {
		USER_DisplayName = uSER_DisplayName;
	}
	public String getUSER_UserName() {
		return USER_UserName;
	}
	public void setUSER_UserName(String uSER_UserName) {
		USER_UserName = uSER_UserName;
	}
	public String getUSER_Password() {
		return USER_Password;
	}
	public void setUSER_Password(String uSER_Password) {
		USER_Password = uSER_Password;
	}
	public int getUSER_idRole() {
		return USER_idRole;
	}
	public void setUSER_idRole(int uSER_idRole) {
		USER_idRole = uSER_idRole;
	}
}
